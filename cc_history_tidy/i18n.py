_current_language = "zh"


def set_language(code: str) -> None:
    global _current_language
    _current_language = code if code in LANGS else "en"


def current_language() -> str:
    return _current_language


def tr(key: str, **kwargs) -> str:
    table = LANGS.get(_current_language, LANGS["en"])
    template = table.get(key) or LANGS["en"].get(key) or LANGS["zh"][key]
    return template.format(**kwargs) if kwargs else template


def detect_default_language(settings_path: Path) -> str:
    try:
        data = json.loads(Path(settings_path).read_text(encoding="utf-8"))
        language = data.get("language") if isinstance(data, dict) else None
        if language in LANGS:
            return language
    except (OSError, json.JSONDecodeError):
        pass
    try:
        from PySide6.QtCore import QLocale

        name = QLocale.system().name()  # e.g. "zh_CN", "zh_TW", "de_DE"
    except Exception:  # pragma: no cover - PySide missing in exotic envs
        return "en"
    if name.startswith(("zh_TW", "zh_HK", "zh_MO", "zh_Hant")):
        return "zh_TW"
    base = name.split("_")[0]
    if base == "zh":
        return "zh"
    if base in LANGS:
        return base
    return "en"


def save_language(code: str, settings_path: Path) -> None:
    settings_path = Path(settings_path)
    data: dict[str, object] = {}
    try:
        loaded = json.loads(settings_path.read_text(encoding="utf-8"))
        if isinstance(loaded, dict):
            data = loaded
    except (OSError, json.JSONDecodeError):
        pass
    data["language"] = code
    settings_path.parent.mkdir(parents=True, exist_ok=True)
    settings_path.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
